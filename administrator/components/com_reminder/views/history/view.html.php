/**
* HTML View class for the Reminder component
*
* @package	Reminder
* @subpackage	History
*/
class ReminderViewHistory extends ReminderClassView
{
	/**
	* List of the reachables layouts. Fill this array in every view file.
	*
	* @var array
	*/
	protected $layouts = array('history');

	/**
	* Execute and display a template : History
	*
	* @access	protected
	* @param	string	$tpl	The name of the template file to parse; automatically searches through the template paths.
	*
	*
	* @since	11.1
	*
	* @return	mixed	A string if successful, otherwise a JError object.
	*/
	protected function displayHistory($tpl = null)
	{
		// Initialiase variables.
		$this->model	= $model	= $this->getModel();
		$this->state	= $state	= $this->get('State');
		$this->params 	= $state->get('params');
		$state->set('context', 'layout.history');
		$this->item		= $item		= $this->get('Item');

		$this->form		= $form		= $this->get('Form');
		$this->canDo	= $canDo	= ReminderHelper::getActions($model->getId());
		$lists = array();
		$this->lists = &$lists;

		// Define the title
		$this->_prepareDocument(JText::_('REMINDER_LAYOUT_HISTORY'), $this->item, 'customer_id');

		$user		= JFactory::getUser();
		$isNew		= ($model->getId() == 0);

		//Check ACL before opening the form (prevent from direct access)
		if (!$model->canEdit($item, true))
			$model->setError(JText::_('JERROR_ALERTNOAUTHOR'));

		// Check for errors.
		if (count($errors = $model->getErrors()))
		{
			JError::raiseError(500, implode(BR, array_unique($errors)));
			return false;
		}
		//Toolbar
		JToolBarHelper::title(JText::_('REMINDER_LAYOUT_HISTORY'), 'pencil-2');

		// Save & Close
		if (($isNew && $model->canCreate()) || (!$isNew && $item->params->get('access-edit')))
			JToolBarHelper::save('history.save', "REMINDER_JTOOLBAR_SAVE_CLOSE");
		// Save
		if (($isNew && $model->canCreate()) || (!$isNew && $item->params->get('access-edit')))
			JToolBarHelper::apply('history.apply', "REMINDER_JTOOLBAR_SAVE");
		// Cancel
		JToolBarHelper::cancel('history.cancel', "REMINDER_JTOOLBAR_CANCEL");
		$model_customer_id = CkJModel::getInstance('Customers', 'ReminderModel');
		$model_customer_id->addGroupOrder("a.domain");
		$lists['fk']['customer_id'] = $model_customer_id->getItems();
	}


}



